async function saveReport(
  systemPrompt: string,
  showsList: string,
  result: Omit<EvalRunResult, 'reportUrl'>
): Promise<string> {
  const reportsDir = path.join(process.cwd(), 'public', 'eval-reports')
  await fs.mkdir(reportsDir, { recursive: true })

  const now = new Date()
  const runAt = now.toLocaleString('en-IE', {
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
    hour: '2-digit',
    minute: '2-digit',
    second: '2-digit',
    hour12: false,
  })
  const slug = now
    .toISOString()
    .replace(/[:.]/g, '-')
    .replace('Z', '')
  const filename = `eval-${slug}-${result.grade}-${result.overallScore}.html`

  // Write individual report
  const html = generateReportHtml(runAt, systemPrompt, showsList, result)
  await fs.writeFile(path.join(reportsDir, filename), html, 'utf-8')

  // Update manifest
  const manifestPath = path.join(reportsDir, 'manifest.json')
  let manifest: ManifestEntry[] = []
  try {
    const raw = await fs.readFile(manifestPath, 'utf-8')
    manifest = JSON.parse(raw)
  } catch {
    // first run — no manifest yet
  }
  manifest.unshift({ filename, runAt, grade: result.grade, overallScore: result.overallScore })
  await fs.writeFile(manifestPath, JSON.stringify(manifest, null, 2), 'utf-8')

  // Regenerate index
  const indexHtml = generateIndexHtml(manifest)
  await fs.writeFile(path.join(reportsDir, 'index.html'), indexHtml, 'utf-8')

  return `/eval-reports/${filename}`
}

export async function POST(req: NextRequest) {
  try {
    const body = await req.json()
    const { systemPrompt, showsList, count = 8 } = body as {
      systemPrompt: string
      showsList: string
      count: number
    }

    if (!systemPrompt?.trim()) {
      return NextResponse.json({ error: 'System prompt is required' }, { status: 400 })
    }
    if (!showsList?.trim()) {
      return NextResponse.json({ error: 'Shows list is required' }, { status: 400 })
    }
    if (count < 1 || count > 20) {
      return NextResponse.json({ error: 'Count must be between 1 and 20' }, { status: 400 })
    }

    const userContent = `My favourite shows:\n${showsList}\n\nPlease return ${count} recommendations.`

    // Step 1: Generate recommendations
    const recMessage = await anthropic.messages.create({
      model: 'claude-sonnet-4-6',
      max_tokens: 4096,
      system: systemPrompt,
      messages: [{ role: 'user', content: userContent }],
    })

    const recText = recMessage.content[0].type === 'text' ? recMessage.content[0].text : ''
    let parsed: RecommendationsResponse
    try {
      parsed = JSON.parse(stripMarkdownFences(recText))
    } catch {
      return NextResponse.json(
        { error: 'Failed to parse recommendations response', details: recText.slice(0, 200) },
        { status: 500 }
      )
    }

    // Step 2: Judge the recommendations
    const judgeUserContent =
      `Input shows (the viewer's favourites):\n${showsList}\n\n` +
      `Recommendations to evaluate:\n${JSON.stringify(parsed.recommendations)}`

    const judgeMessage = await anthropic.messages.create({
      model: 'claude-sonnet-4-6',
      max_tokens: 1024,
      system: EVAL_JUDGE_SYSTEM_PROMPT,
      messages: [{ role: 'user', content: judgeUserContent }],
    })

    const judgeText = judgeMessage.content[0].type === 'text' ? judgeMessage.content[0].text : ''
    let judgeResponse
    try {
      judgeResponse = JSON.parse(stripMarkdownFences(judgeText))
    } catch {
      return NextResponse.json(
        { error: 'Failed to parse judge response', details: judgeText.slice(0, 200) },
        { status: 500 }
      )
    }

    // Step 3: Compute scores and grade
    const c = judgeResponse.criteria
    const scores = [
      c.relevance.score ?? 0,
      c.reasoningQuality.score ?? 0,
      c.diversity.score ?? 0,
      c.metadataAccuracy.score ?? 0,
      c.noOverlap.score ?? 0,
    ]
    const overallScore = computeOverallScore(scores)
    const grade = scoreToGrade(overallScore)

    const criteria: EvalCriteria = {
      relevance: c.relevance,
      reasoningQuality: c.reasoningQuality,
      diversity: c.diversity,
      metadataAccuracy: c.metadataAccuracy,
      noOverlap: c.noOverlap,
    }

    const partial = { recommendations: parsed.recommendations, criteria, overallScore, grade, rawCritique: judgeResponse.critique }

    // Step 4: Save HTML report and update index
    const reportUrl = await saveReport(systemPrompt, showsList, partial)

    const result: EvalRunResult = { ...partial, reportUrl }
    return NextResponse.json(result)
  } catch (err) {
    const errorMessage = err instanceof Error ? err.message : 'Unknown error'
    return NextResponse.json({ error: errorMessage }, { status: 500 })
  }
}
